#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(void *))
{
	if (lst == NULL || f == NULL)
		return ;
	while (lst->next != '\0')
	{
		f(lst->content);
		lst = lst->next;
	}
}
